for i in *.md; do
    sed -ri "s/'''/\*\*/g" $i
    sed -ri "s/''/\*/g" $i
    sed -ri "s/=====(.*)=====/##### \1/g" $i
    sed -ri "s/====(.*)====/#### \1/g" $i
    sed -ri "s/===(.*)===/### \1/g" $i
    sed -ri "s/==(.*)==/## \1/g" $i
    sed -ri "s/=(.*)=/# \1/g" $i
    sed -ri "s/\[([A-Z0-9a-Z/:_.%?=#!\-]+) ([A-Za-z_,0-9 +'\-]+)\]([^]])/\[\2\]\(\1\)\3/g" $i
    sed -ri "s/\[([A-Z0-9a-Z/:_.%?=#!\-]+) ([A-Za-z_,0-9 +'\-]+)\]$/\[\2\]\(\1\)/g" $i
done
